    public void DeleteUser() throws MalformedURLException, IOException {

        String apiUrl = "https://black-diamond.com.co/API/public/api/users/" + id + "?key=EXaR0JoKIirohPwbRPIHc3s73Oygi0XV";
        URL url = new URL(apiUrl);
        HttpURLConnection conn = (HttpURLConnection) url.openConnection();
        //Method delete
        conn.setRequestMethod("DELETE");
        conn.setRequestProperty("Content-Type", "application/x-www-form-urlencoded");

        Map<String, Object> data = new LinkedHashMap<>();
        data.put("param_state", "1652");

        StringBuilder deleteData = new StringBuilder();
        for (Map.Entry<String, Object> param : data.entrySet()) {
            if (deleteData.length() != 0) {
                deleteData.append('&');
            }

            deleteData.append(URLEncoder.encode(param.getKey(), "UTF-8"));
            deleteData.append('=');
            deleteData.append(URLEncoder.encode(String.valueOf(param.getValue()), "UTF-8"));
        }

        byte[] deleteDataBytes = deleteData.toString().getBytes("UTF-8");
        conn.setRequestProperty("Content-Length", String.valueOf(deleteDataBytes.length));
        conn.setDoOutput(true);
        conn.getOutputStream().write(deleteDataBytes);

        Reader in = new BufferedReader(new InputStreamReader(conn.getInputStream(), "UTF-8"));
        for (int c = in.read(); c != -1; c = in.read()) {
            System.out.print((char) c);
        }
    }
